CREATE TABLE employees_history (employee_id number(6),
                                first_name varchar2(20),
                                last_name varchar2(25),
                                email varchar2(25),
                                phone_number varchar2(20),
                                hire_date date,
                                job_id varchar2(10),
                                salary number(8,2),
                                commission_pct number(2,2),
                                manager_id number(6),
                                department_id number(4),
                                fire_date date);    
               
